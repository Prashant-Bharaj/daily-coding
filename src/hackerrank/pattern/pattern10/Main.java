package hackerrank.pattern.pattern10;

import java.util.*;

public class Main{

public static void main(String[] args) {
    Scanner scn = new Scanner(System.in);
    int number = scn.nextInt();
    for(int i = 1; i <= number; i++){
        for(int j = 1; j <= number; j++){
            System.out.println();
        }
        System.out.println();
    }
 }
}