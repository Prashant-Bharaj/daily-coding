package googleKickStart.newPassword;

import java.io.*;
import java.util.*;

public class Solution {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        int T = in.nextInt();
        for (int t = 1; t <= T; t++) {
            int n = 0;
            System.out.printf("Case #%d: %d\n", t, n);
        }
        in.close();
    }
}